<h2>Programmi "Mõõtühikute teisendamine" infofail</h2>

<b>Kuidas kasutada & millega tegu</b><br />
Programm teisendab pikkuse, pindala, mahu, kaalu ja temperatuuriga seotud mõõtühikuid.
Peale programmi käivitamist tuleb valida sobiv tüüp. Seejärel saab sisestada olemasleva mõõtühiku ja selle hulga
ning valida, millisesse mõõtühikusse soovitakse see teisendada.<br>
